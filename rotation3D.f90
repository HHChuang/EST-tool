if (res(4).eq.0.D0) then
  res(1:3) = (/ 0.D0, 0.D0, 1.D0 /)
  return
else
! set some initial LAPACK variables 
 nn = 3
 LDA = nn
 LDVL = nn
 LDVR = nn
 INFO = 0
 
! first initialize the parameters for the LAPACK DGEEV routines
